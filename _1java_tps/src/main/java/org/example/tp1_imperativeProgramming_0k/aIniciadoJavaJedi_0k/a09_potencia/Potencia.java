package org.example.tp1_imperativeProgramming_0k.aIniciadoJavaJedi_0k.a09_potencia;

/* 9. Power of 2 and 3: Store a number and then calculate the power of that number squared and cubed. */

public class Potencia {
  public static void main(String[] args) {
    int num1 = 2;
    int pow2 = num1 * num1;
    int pow3 = pow2 * num1;

    System.out.println("num1: " + num1);
    System.out.println("pow2: " + pow2);
    System.out.println("pow3: " + pow3);
  }
}
